#include "minishell.h"

t_token	*new_token(t_token_type type, char *value)
{
	t_token	*tok;

	tok = malloc(sizeof(t_token));
	if (!tok)
		return (NULL);
	tok->type = type;
	tok->value = value;
	tok->next = NULL;
	return (tok);
}

void	token_add_back(t_token **lst, t_token *new_tok)
{
	t_token	*cur;

	if (!lst || !new_tok)
		return ;
	if (!*lst)
	{
		*lst = new_tok;
		return ;
	}
	cur = *lst;
	while (cur->next)
		cur = cur->next;
	cur->next = new_tok;
}
